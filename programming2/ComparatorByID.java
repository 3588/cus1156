package programming2;

import java.util.Comparator;

/**
 * The ComparatorByID class, which implements the Comparator interface.
 * Sorts an arrayList of courses by just their IDs.
 * @author Patrick Steel
 *
 */
public class ComparatorByID implements Comparator<Course>{
	public int compare(Course o1, Course o2){
		return o1.courseID.compareTo(o2.courseID);
	}
}
